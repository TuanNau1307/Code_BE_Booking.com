    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        RoomModel r = new RoomModel();
        r.setMaLoaiPhong(Integer.parseInt(txtID.getText()));
        r.setSoPhong(Integer.parseInt(txtSoPhong.getText()));
        r.setTenLoaiPhong(txtTenLoaiPhong.getText());
        r.setSoGiuong(txtSoGiuong.getText());
        r.setGiaPhong(Integer.parseInt(txtGiaPhong.getText()));
        r.setMoTa(txtMoTa.getText());
        r.setSoNguoi(Integer.parseInt(txtSoNguoi.getText()));
        r.setMaKhachSan(Integer.parseInt(txtMaKhachSan.getText()));
        if (roomDAO.editLoaiPhong(r, txtID.getText())) {
            roomDAO.editLoaiPhong(r, txtID.getText());
            JOptionPane.showMessageDialog(rootPane, "Cập nhật phòng thành công!");
            list.clear();
            list = roomDAO.getListLoaiPhong();
            showResult();
            //reset text field
            txtID.setEditable(true);
            txtID.setText("");
            txtSoPhong.setText("");
            txtTenLoaiPhong.setText("");
            txtSoGiuong.setText("");
            txtGiaPhong.setText("");
            txtSoNguoi.setText("");
            txtMoTa.setText("");
            txtMaKhachSan.setText("");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật phòng không thành công!");
        }

    }//GEN-LAST:event_updateActionPerformed

