    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        if (txtSoPhong.getText().equals("") || txtTenLoaiPhong.getText().equals("")
                || txtSoGiuong.getText().equals("") || txtGiaPhong.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Hãy điền đầy đủ thông tin của loai phòng!");
        } else {
            RoomModel r = new RoomModel();
            //r.setMaLoaiPhong(Integer.parseInt(txtID.getText()));
            r.setSoPhong(Integer.parseInt(txtSoPhong.getText()));
            r.setTenLoaiPhong(txtTenLoaiPhong.getText());
            r.setSoGiuong(txtSoGiuong.getText());
            r.setSoNguoi(Integer.parseInt(txtSoNguoi.getText()));
            r.setGiaPhong(Integer.parseInt(txtGiaPhong.getText()));
            r.setMaKhachSan(Integer.parseInt(txtMaKhachSan.getText()));
            r.setMoTa(txtMoTa.getText());
            if (roomDAO.addLoaiPhong(r)) {
                JOptionPane.showMessageDialog(rootPane, "Thêm loai phòng thành công!");
                list.add(r);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Thêm loai phòng không thành công, hãy kiểm tra lại thông tin!");
            }
            showResult();
        }
    }//GEN-LAST:event_addActionPerformed

    public void showResult() {
        model.setRowCount(0);
        int i = 1;
        for (RoomModel r : list) {
            model.addRow(new Object[]{
                i++, r.getMaLoaiPhong(), r.getTenLoaiPhong(),r.getMoTa(),r.getSoGiuong(),r.getSoNguoi(),r.getSoPhong(),r.getGiaPhong(),r.getMaKhachSan()
            });
        }
    }

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        String id = tblRoom.getValueAt(tblRoom.getSelectedRow(), 1).toString();
        int DelIndex = tblRoom.getSelectedRow();
        if (roomDAO.deleteLoaiPhong(id)) {
            list.remove(DelIndex);
            showResult();
            JOptionPane.showMessageDialog(rootPane, "Xóa phòng thành công");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Xóa phòng không thành công");
        }

    }//GEN-LAST:event_deleteActionPerformed


